package Merve.week2;

public class SwapNumbers {

    public static void main(String[] args) {
        int x = 5;
        int y= 10;

        x = x + y;   // x=5+10  x=15
        y= x - y;    // y=15-10 y=5
        x= x-y;     //  x=15-5  x=10;

        System.out.println("x = " + x);
        System.out.println("y = " + y);

    }


}
